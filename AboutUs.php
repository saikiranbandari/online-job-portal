<p>The aim of this project is to develops an online search Portal for the
Placement Dept. of the college. The system is an online application that can be
accessed throughout the organization and outside as well with proper login
provided. This system can be used as an Online Job Portal for the Placement Dept
of the college to manage the student information with regards to placement.
Students logging should be able to upload their information in the form of a CV.
Visitors/Company representatives logging in may also access/search any
information put up by Students.
The project has been planned to be having the view of distributed
architecture, with centralized storage of the database. The application for the
storage of the data has been planned. Using the constructs of MS-SQL Server and
all the user interfaces have been designed using the ASP.Net technologies. The
database connectivity is planned using the “SQL Connection” methodology. The
standards of security and data protective mechanism have been given a big choice
for proper usage. The application takes care of different modules and their
associated reports, which are produced as per the applicable strategies and
standards that are put forwarded by the administrative staff.<p>
